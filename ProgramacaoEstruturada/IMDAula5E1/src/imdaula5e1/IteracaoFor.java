public class IteracaoFor {

    /**
     * @param args the command line arguments
     * 
     * Algoritmo mostra o funcionamento do comando de iteração FOR.
     */
    public static void main(String[] args) {
        for (int i=0; i<10; i++){
            System.out.println(+(i+1)+" Iniciando estudo dos comandos de iteração!!");
        }
    }
    
}
