public class ProgramaFor {

    /**
     * @param args the command line arguments
     * 
     * Algoritmo conta de 1 a 10
     */
    public static void main(String[] args) {
        int i = 1;
		while (i <= 10) {
			System.out.println(i);
			i = i + 1;
		}
    }
    
}
